package com.example.oneone;

public class WiFi_Item{

    private double X ;
    private double Y ;

    public void setGpsX(double title) {
        X = title ;
    }
    public void setGpsY(double desc) {
        Y = desc ;
    }

    public double getGpsX() {
        return this.X ;
    }
    public double getGpsY() {
        return this.Y ;
    }


}
